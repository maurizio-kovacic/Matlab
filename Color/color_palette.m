function [P] = color_palette(Name)
if( nargin < 1 )
    Name = [];
end
persistent PData;
PData = {'black',  [0 0 0; 1 1 1];
         'brown',  hex2rgb(['#7f4d3e';'#b87c4c';'#e2b659';'#f9f871']);...
         'orange', flipud(hex2rgb(['#ffffd4';'#fed98e';'#fe9929';'#d95f0e';'#993404']));...
         'blue',   rot90(hex2rgb(['#ffffd4';'#fed98e';'#fe9929';'#d95f0e';'#993404']),2);...
         'green',  flipud(hex2rgb(['#ffffcc';'#c2e699';'#78c679';'#31a354';'#006837']));...
         'purple', flipud(hex2rgb(['#edf8fb';'#b3cde3';'#8c96c6';'#8856a7';'#810f7c']));...
         'sign',   flipud(hex2rgb(['#ca0020';'#f4a582';'#f7f7f7';'#92c5de';'#0571b0']));...
         'king',   hex2rgb(['#1a2a6c';'#b21f1f';'#fdbb2d']);...
         'vision', hex2rgb(['#000046';'#1cb5e0']);...
         'turbo',  flipud(hex2rgb(['#f1f2b5';'#135058']));...
         'pinot',  flipud(hex2rgb(['#4b6cb7';'#182848']));...
         'sky',    hex2rgb(['#076585';'#ffffff']);...
         'aqua',   hex2rgb(['#13547a';'#80d0c7']);...
         'dusk',   flipud(hex2rgb(['#ffd89b';'#19547b']));...
         'relay',  hex2rgb(['#3a1c71';'#d76d77';'#ffaf7b']);...
         'sweet',  hex2rgb(['#3f51b1';'#5a55ae';'#7b5fac';'#8f6aae';'#a86aa4';'#cc6b8e';'#f18271';'#f3a469';'#f7c978']);...
         'phoenix',hex2rgb(['#f83600';'#f9d423']);...     
         'RYB',    hex2rgb(['#FE2712';'#FC600A';'#FB9902';'#FCCC1A';'#FEFE33';'#B2D732';'#66B032';'#347C98';'#0247FE';'#4424D6';'#8601AF';'#C21460']);};
     
     
if( isempty(Name) )
    P = PData;
else
    i = [];
    if( ischar(Name) || isstring(Name) )
        i = find( strcmpi(PData(:,1),Name) );
    else
        if( Name > 0 && Name <= row(PData) )
            i = Name;
        end
    end
    P = [];
    if( ~isempty(i) )
        P = cell2mat(PData(i,2));
    end
end
end